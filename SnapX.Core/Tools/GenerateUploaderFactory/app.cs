static string FindRepoRoot(string? startPath = null)
{
	startPath ??= Environment.CurrentDirectory;

	var dir = File.Exists(startPath)
		? new DirectoryInfo(Path.GetDirectoryName(Path.GetFullPath(startPath))!)
		: new DirectoryInfo(Path.GetFullPath(startPath));

	while (true)
	{
		try
		{
			if (File.Exists(Path.Combine(dir.FullName, "build.sh")))
				return dir.FullName;
		}
		catch (UnauthorizedAccessException)
		{
			// Skip directories we can't read
		}
		catch (IOException)
		{
			// Ignore transient filesystem issues
		}

		if (dir.Parent == null)
			break;

		dir = dir.Parent;
	}

	throw new InvalidOperationException(
		"Could not locate repo root (build.sh not found). "
			+ "Make sure this tool is executed from inside the SnapX repository."
	);
}

var root =
	FindRepoRoot(Environment.CurrentDirectory)
	?? throw new InvalidOperationException("Could not locate repo root (build.sh not found).");

string sourceRoot = Path.Combine(root, "SnapX.Core");
string uploaderFactoryPath = Path.Combine(sourceRoot, "Upload", "UploaderFactory.cs");

if (File.Exists(uploaderFactoryPath))
{
	try
	{
		File.Delete(uploaderFactoryPath);
	}
	catch
	{
		// Sillenceee
	}
	// return;
}

var regex = new Regex(
	@"public\s+class\s+(?<name>\w+UploaderService)\s*:\s*\w*UploaderService(?:\s*<[^>]+>)?",
	RegexOptions.Multiline | RegexOptions.Compiled
);

// Scan all .cs files for subclasses of UploaderService<>
var uploaderFiles = Directory
	.GetFiles(Path.Combine(sourceRoot, "Upload"), "*.cs", SearchOption.AllDirectories)
	.Where(f =>
	{
		if (f.EndsWith("UploaderFactory.cs", StringComparison.OrdinalIgnoreCase))
			return false;

		var content = File.ReadAllText(f);

		// Match lines like "class X : SomethingUploaderService" or "class X : SomethingUploaderService<...>"
		return regex.Match(content).Success;
	})
	.ToList();

Console.WriteLine($"Found {uploaderFiles.Count} UploaderService subclasses");
var registrations = new List<string>();

foreach (var file in uploaderFiles)
{
	var content = File.ReadAllText(file);
	var matches = regex.Matches(content);
	foreach (Match match in matches)
	{
		if (!match.Success)
			continue;
		var className = match.Groups["name"].Value.Trim();
		registrations.Add(className);
	}
}

if (!registrations.Any())
{
	Console.WriteLine("No uploader subclasses found.");
	return;
}
